from math import *

# abs() এবসুলেট নাম্বার রিটার্ন করে 
number = -5
print(abs(number))

# max() ম্যাক্স ভ্যালু রিটার্ন করে 
print(max(4,5))

# mix() মিনিমাম ভ্যালু রিটার্ন করে 
print(min(4,6))

# pow() দুটি আর্গুমেন্ট নেয় এবং ২য় ভ্যালু কে প্রথম ভ্যালুর পাওয়ার হিসবে কাউন্ট করে ভ্যালু রিটার্ন করে 
print(pow(3,3))

# round() দশমিক নাম্বার করে পূর্ন সংখ্যা হিসেবে রিটার্ন করে। দশমিকের পরে পাঁচের  বেশি থাকলে কিংবা পাঁচের কম থাকলেও, তাকে পূর্ন সংখ্যা হিসাবে ভ্যালু রিটার্ন করে 
print(round(3.7))

