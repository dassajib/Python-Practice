# লিস্ট নিয়ে আমরা আগে কাজ করার সময় শুধু একটি লিস্ট নিয়ে কাজ করেছিলাম,এবার আমরা লিস্টের ভিতরে লিস্ট নিয়ে কাজ করবো 

numbers = [
    [999,888,111],
    [333,555,777],
    [111,555,666]
]
# লিস্টের মধ্যে নির্দিষ্ট ইনডেক্সের ভ্যালু বের করা 
print(numbers[1][2])
# লিস্টের মধ্যে ভ্যালু পরিবর্তন 
numbers[0][1] = 789
print(numbers)

# লিস্ট এর মধ্যে লিস্ট গুলার ডেটা ওয়ান বাই ওয়ান শো 
for number in numbers:
    for num in number:
        print(num)